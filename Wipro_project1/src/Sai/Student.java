package Sai;

public class Student {
    String name;
    int english;
    int maths;
    int total;
    double percentage;

    Student(String name, int english, int maths, int total, double percentage) {
        this.name = name;
        this.english = english;
        this.maths = maths;
        this.total = total;
        this.percentage = percentage;
    }
}
